package learnOperations;

public class AssignmentOperator {
	
	int age = 25;
	void findAgeAfter5Year() {
		age+=5; //add equal to
		System.out.println("Age after 5 year = "+age);
	}
	
	void findAgeBefore10Year() {
		age-=10; //add equal to
		System.out.println("Age before 10 year = "+age);
	}
	
	void findAgeInHalf() {
		age/=10; //add equal to
		System.out.println("Age in half = "+age);
	}
			
}
